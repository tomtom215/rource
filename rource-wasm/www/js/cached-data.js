// Pre-calculate stats for the cached Rource data
// Count unique (timestamp, author) pairs to match what the WASM parser counts
// This ensures consistency between the stats display and the timeline
export const ROURCE_STATS = (() => {
    const lines = ROURCE_CACHED_DATA.split('\n');
    const files = new Set();
    const authors = new Set();
    const commits = new Set(); // Track unique (timestamp, author) pairs
    let lastTimestamp = 0;
    for (const line of lines) {
        if (!line.trim()) continue;
        const parts = line.split('|');
        if (parts.length >= 4) {
            const ts = parseInt(parts[0], 10);
            const author = parts[1].trim();
            commits.add(`${ts}|${author}`); // Count unique (timestamp, author) as commits
            authors.add(author);
            files.add(parts[3].trim());
            if (ts > lastTimestamp) lastTimestamp = ts;
        }
    }
    // Use visualization commit count for consistency with timeline
    return { commits: commits.size, files: files.size, authors: authors.size, lastTimestamp };
})();

// Track additional commits fetched via refresh
let additionalCommits = '';

/**
 * Gets additional commits that have been fetched.
 * @returns {string} Additional commit data
 */
export function getAdditionalCommits() {
    return additionalCommits;
}

/**
 * Sets additional commits (appended to cached data).
 * @param {string} data - Additional commit data
 */
export function setAdditionalCommits(data) {
    additionalCommits = data;
}

/**
 * Appends new commits to the additional commits buffer.
 * @param {string} data - New commit data to append
 */
export function appendAdditionalCommits(data) {
    if (data) {
        additionalCommits = additionalCommits
            ? additionalCommits + '\n' + data
            : data;
    }
}

/**
 * Gets the full cached data including any additional commits.
 * @returns {string} Complete commit data
 */
export function getFullCachedData() {
    return additionalCommits
        ? ROURCE_CACHED_DATA + '\n' + additionalCommits
        : ROURCE_CACHED_DATA;
}

