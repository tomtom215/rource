/**
 * Sets additional commits (appended to cached data).
 * @param {string} data - Additional commit data
 */
export function setAdditionalCommits(data) {
    additionalCommits = data;
}

/**
 * Appends new commits to the additional commits buffer.
 * @param {string} data - New commit data to append
 */
export function appendAdditionalCommits(data) {
    if (data) {
        additionalCommits = additionalCommits
            ? additionalCommits + '\n' + data
            : data;
    }
}

/**
 * Gets the full cached data including any additional commits.
 * @returns {string} Complete commit data
 */
export function getFullCachedData() {
    return additionalCommits
        ? ROURCE_CACHED_DATA + '\n' + additionalCommits
        : ROURCE_CACHED_DATA;
}

